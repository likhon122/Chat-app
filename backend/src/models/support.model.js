import mongoose from "mongoose";

const SupportModel = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true
    },
    email: {
      type: String,
      required: true
    },
    message: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Support = mongoose.model("Support", SupportModel);

export default Support;
